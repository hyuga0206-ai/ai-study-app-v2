export function getQuestionsByChapter(chapterId: number): Question[] {
  if (chapterId === 0) return questions
  return questions.filter((q) => q.chapterId === chapterId)
}

export function getQuestion(id: string): Question | undefined {
  return questions.find((q) => q.id === id)
}

export function getWrongQuestions(wrongIds: string[]): Question[] {
  return questions.filter((q) => wrongIds.includes(q.id))
}
